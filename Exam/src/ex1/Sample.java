package ex1;

public class Sample {

	public static void main(String[] args) {
		int num1 = 10; // 정수형 타입 변수 num1에 숫자데이터 10 저장  
		// 변수 선언후 처음 데이터를 저장하는 것을 "초기화"라고 한다. 
		int num2 = 20; // 정수형 타입 변수 num2에 숫자 20 저장
		int num3 = num1 + num2; // 정수형 타입 변수 num3 선언, num+1과 num2결과로 정하기
		System.out.println(num1+ "+"+ num2 + "="+ num3);// 연산의 결과를 출력

	}

}
